#include <iostream>

//------------------------------------------------------------------------------
void print(int* arr, int size)
{
    for (int i = 0; i < size; i++) {
        std::cout << arr[i] << " ";
    }
    std::cout << std::endl;
}
//------------------------------------------------------------------------------
int main()
{
    int arr[10] = { 1, 4, 6, 6, 4, 6, 8, 4, 3, 2 };
    int arr2[5] = { 1, 4, 4, 3, 2 };
    int arr3[7] = { 1, 4, 6, 8, 4, 3, 2 };
    int arr4[2] = { 3, 2 };

    print(arr, 10);
    print(arr2, 5);
    print(arr3, 7);
    print(arr4, 2);

    return 0;
}
